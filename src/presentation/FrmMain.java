    /* Charger listeEmployes (modèle) et lstEmployes (vue) avec la liste des Employes */
    private void chargerEmployes () {
        listeEmployes = new ListeEmployes();
        for (int k = 0; k < listeEmployes.size(); k++) {
            Employe e = listeEmployes.getEmploye(k);
            lstEmployes.add(e.getNomEmpl() + " " + e.getPrenomEmpl());
        }
    } // chargerEmployes

    /* Afficher les informations de l'employé courant de listeEmployes; vider les champs si l'employé courant n'est pas défini */
    private void afficherEmployeCourant () {
        Employe e = listeEmployes.getEmployeCourant();
        if (e == null) {
            tfNom.setText(""); tfPrenom.setText("");
            tfDateEngagement.setText("");
            tfFonction.setText("");
            tfDepartement.setText(""); tfLieu.setText("");      
        } else {
            tfNom.setText(e.getNomEmpl()); tfPrenom.setText(e.getPrenomEmpl());
            tfDateEngagement.setText(DATE_FORM.format(e.getDateEmpl()));
            tfFonction.setText(e.getFonction().getNomFonc());
            tfDepartement.setText(e.getDepartement().getNomDept()); tfLieu.setText(e.getDepartement().getLieu().getNomLieu());      
        }
    } // afficherEmployeCourant

    /* Gérer l'état d'activation de btnSupprimer */
    private void gereEtatBouton () {
        btnSupprimer.setEnabled(listeEmployes.getPos() != ListeEmployes.NO_POS);
    } // gereEtatBouton

