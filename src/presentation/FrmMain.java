    /** Adjonction d'un nouvel employé à la base. Mise à jour de listeEmployes (modèle) et lstEmployes (vue). */
    public void addEmploye (Employe emp) {
        int noEmpl = EmployeDao.addEmploye(emp); emp.setNoEmpl(noEmpl);
        /* Recherche de la place d'insertion (g) avec l'algorithme de Mauchly (1946) */
        int g = 0; int d = listeEmployes.size() - 1; /* Invariant de la boucle: g-1 <= placeDe(emp) < d+1 */
        while (g <= d) {
            int m = (g + d) >>> 1;   /* int m = (g + d) / 2 */
            if (listeEmployes.getEmploye(m).compareTo(emp) <= 0) {g = m + 1;} else {d = m - 1;}
        }
        listeEmployes.add(emp, g); lstEmployes.add(emp.getNomEmpl() + " " + emp.getPrenomEmpl(), g);
        listeEmployes.setPos(g); lstEmployes.select(g);
        afficherEmployeCourant();
        gereEtatBouton();
    } // addEmploye
  
